function averageAge(array) {
    let sumOfAges = null;
    array.forEach((user) => {
        sumOfAges += user.age;
    });
    let ageCount = null;
    array.forEach((user) => {
        if (user.hasOwnProperty('age')) {
            ageCount++;
        }
    });
    let averageAge = sumOfAges / ageCount;
    return averageAge;
};

